public class StringPractise{
    public static void main(String[] args) {
        // String Creation;
        String s = "Hello";
        System.out.println(s);

        // String Concatenation
        System.out.println(s + " World");

        // String Methods
        System.out.println(s.charAt(3));
        System.out.println(s.concat(" Pravar"));
        
    }
}